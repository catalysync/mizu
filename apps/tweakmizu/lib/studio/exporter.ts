import { resolvePatternsForPlan, type Plan } from './composer';
import type { OutputFile } from '@/lib/patterns/types';

function slugifyName(input: string): string {
  return (
    input
      .toLowerCase()
      .trim()
      .replace(/[^a-z0-9]+/g, '-')
      .replace(/^-+|-+$/g, '')
      .slice(0, 60) || 'tweakmizu-project'
  );
}

