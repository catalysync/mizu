function renderIndexPage(plan: Plan): OutputFile | null {
  const hasIndex = plan.entries.some((entry) => entry.route === '/');
  if (hasIndex) return null;
  return {
    path: 'app/page.tsx',
    contents: `export default function HomePage() {\n  return (\n    <main style={{ padding: '2rem' }}>\n      <h1>${plan.intent.productName}</h1>\n      <p>${plan.intent.description}</p>\n    </main>\n  );\n}\n`,
  };
}

