function renderRootLayout(plan: Plan): OutputFile {
  const safeTitle = plan.intent.productName.replace(/'/g, "\\'");
  const safeDescription = plan.intent.description.replace(/'/g, "\\'");
  const lines = [
    `import type { Metadata } from 'next';`,
    `import './globals.css';`,
    ``,
    `export const metadata: Metadata = {`,
    `  title: '${safeTitle}',`,
    `  description: '${safeDescription}',`,
    `};`,
    ``,
    `export default function RootLayout({ children }: { children: React.ReactNode }) {`,
    `  return (`,
    `    <html lang="en">`,
    `      <body>{children}</body>`,
    `    </html>`,
    `  );`,
    `}`,
    ``,
  ];
  return {
    path: 'app/layout.tsx',
    contents: lines.join('\n'),
  };
}

function renderIndexPage(plan: Plan): OutputFile | null {
  const hasIndex = plan.entries.some((entry) => entry.route === '/');
  if (hasIndex) return null;
  return {
    path: 'app/page.tsx',
    contents: `export default function HomePage() {\n  return (\n    <main style={{ padding: '2rem' }}>\n      <h1>${plan.intent.productName}</h1>\n      <p>${plan.intent.description}</p>\n    </main>\n  );\n}\n`,
  };
}

