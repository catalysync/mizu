function renderReadme(plan: Plan): OutputFile {
  const lines = [
    `# ${plan.intent.productName}`,
    '',
    plan.intent.description,
    '',
    `Generated by **tweakmizu** — industry: \`${plan.intent.industry}\`, stack: \`${plan.intent.stack}\`, tone: \`${plan.intent.tone}\`, density: \`${plan.intent.density}\`.`,
    '',
    '## Pages',
    '',
    ...plan.entries.map((entry) => `- \`${entry.route}\` — ${entry.title} (${entry.patternId})`),
    '',
    '## Getting started',
    '',
    '```bash',
    'pnpm install',
    'pnpm dev',
    '```',
    '',
    'Open http://localhost:3000 to view the site.',
    '',
    '## Tweakmizu substrate',
    '',
    'This project ships with a `.tweakmizu/substrate.json` file describing its design system:',
    'tokens, layout primitives, allowed component variants, and pattern ids. Point your AI',
    'coding tool (Cursor, Claude Code, v0, bolt) at it so subsequent edits inherit the grammar.',
    '',
    `Rationale at generation time: ${plan.rationale}`,
    '',
  ];
  return { path: 'README.md', contents: lines.join('\n') };
}

function renderSubstrate(plan: Plan): OutputFile {
  const substrate = {
    $schema: 'https://tweakmizu.dev/schema/substrate-v1.json',
    substrateVersion: 1,
    project: {
      name: plan.intent.productName,
      industry: plan.intent.industry,
      stack: plan.intent.stack,
      tone: plan.intent.tone,
      density: plan.intent.density,
      generatedAt: plan.createdAt,
      tweakmizuVersion: '0.1.0',
    },
    patterns: plan.entries.map((entry) => ({
      id: entry.patternId,
      route: entry.route,
      title: entry.title,
    })),
    primitives: {
      available: ['Stack', 'Inline', 'Cluster', 'Grid', 'Center', 'Sidebar', 'Split', 'Cover'],
      importPath: '@aspect/react',
    },
    rules: [
      'Use layout primitives (Stack, Inline, Cluster, Grid) instead of manual flex/grid CSS',
      'Use semantic color tokens (surface, text, border, action, feedback)',
      'Do not hardcode pixel values for padding, margin, or gap — use the spacing scale',
      'Prefer mizu components over raw HTML when one exists (Button not <button>)',
      'Import industry-specific components from @aspect/commerce or @aspect/finance when available',
    ],
  };
  return {
    path: '.tweakmizu/substrate.json',
    contents: JSON.stringify(substrate, null, 2) + '\n',
  };
}

