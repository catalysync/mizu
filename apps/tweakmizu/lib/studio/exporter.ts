function dedupeAndSort(files: OutputFile[]): OutputFile[] {
  const map = new Map<string, OutputFile>();
  for (const file of files) {
    map.set(file.path, file);
  }
  return Array.from(map.values()).sort((a, b) => {
    if (a.path < b.path) return -1;
    if (a.path > b.path) return 1;
    return 0;
  });
}

export function buildProjectFiles(plan: Plan): OutputFile[] {
  const patterns = resolvePatternsForPlan(plan);
  const ctx = {
    productName: plan.intent.productName,
    description: plan.intent.description,
  };

  const files: OutputFile[] = [];

  files.push(renderPackageJson(plan));
  files.push(renderReadme(plan));
  files.push(renderSubstrate(plan));
  files.push(renderTsconfig());
  files.push(renderNextConfig());
  files.push(renderGlobalsCss());
  files.push(renderRootLayout(plan));
  files.push(renderGitignore());
  files.push(renderLicense());

  const indexPage = renderIndexPage(plan);
  if (indexPage) files.push(indexPage);

  for (const pattern of patterns) {
    const out = pattern.renderReact(ctx);
    for (const file of out) {
      files.push(file);
    }
  }

  return dedupeAndSort(files);
}

export async function buildProjectZip(plan: Plan): Promise<Blob> {
  const { default: JSZip } = await import('jszip');
  const zip = new JSZip();
  const files = buildProjectFiles(plan);
  for (const file of files) {
    zip.file(file.path, file.contents);
  }
  return zip.generateAsync({ type: 'blob' });
}

export function downloadBlob(blob: Blob, filename: string) {
  const url = URL.createObjectURL(blob);
  const anchor = document.createElement('a');
  anchor.href = url;
  anchor.download = filename;
  document.body.appendChild(anchor);
  anchor.click();
  document.body.removeChild(anchor);
  setTimeout(() => URL.revokeObjectURL(url), 1000);
}
