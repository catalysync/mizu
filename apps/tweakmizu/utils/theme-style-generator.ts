/**
 * Generate a minimal diff — only the overrides from the preset.
 */
export function generateDiffCode(
  state: ThemeEditorState,
  colorFormat: ColorFormat = 'hex',
): string {
  const { styles, preset } = state;
  const selector = preset ? `[data-mizu-identity="${preset}"]` : ':root';

  return generateCssBlock(selector, styles, colorFormat, true) || '/* No overrides */';
}
